package polygon

import "testing"

func TestSticks(t *testing.T) {
	var s *Shapes
	s = s.Builder(
		/* poly0 ["7"] */ []Point{
			{1.95208276, -0.76872245},
			{2.25844355, -0.84531264},
			{3.48651373, 4.06696806},
			{3.18015294, 4.14355826},
		}...).Builder(
		/* poly1 ["10"] */ []Point{
			{1.05263158, 2.01754386},
			{3.50877193, 2.01754386},
			{3.50877193, 2.33333333},
			{1.05263158, 2.33333333},
		}...).Builder(
		/* poly2 ["13"] */ []Point{
			{0.87719298, 0.96491228},
			{3.33333333, 0.96491228},
			{3.33333333, 1.28070175},
			{0.87719298, 1.28070175},
		}...)
	s.Union()
	if len(s.P[0].PS) != 20 {
		t.Fatalf("wanted 20 merged points, got %d", len(s.P[0].PS))
	}
}
