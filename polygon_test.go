// debugPoly generates a text dump of the
func debugPoly(gocode bool, s *Shapes) {
	for i, p := range s.P {
		name := fmt.Sprint("poly", i)
		if gocode {
			fmt.Printf("\t/* %s [%q] */ []Point{\n", name, p.Index)
		} else {
			fmt.Printf("%s = # %q\n", name, p.Index)
		}
		for _, pt := range p.PS {
			if gocode {
				fmt.Printf("\t\t{%.6f, %.6f},\n", pt.X, pt.Y)
			} else {
				fmt.Printf("  %.6f %.6f\n", pt.X, pt.Y)
			}
		}
		if gocode {
			fmt.Println("\t},")
		} else {
			fmt.Println("end")
		}
	}
}

// validate that the bounding box for a polygon is correct.
func (p *Shapes) chk(n int) error {
	p1 := p.P[n]
	var ll, tr Point
	for i, pt := range p1.PS {
		if i == 0 {
			ll = pt
			tr = pt
			continue
		}
		if pt.X < ll.X {
			ll.X = pt.X
		} else if pt.X > tr.X {
			tr.X = pt.X
		}
		if pt.Y < ll.Y {
			ll.Y = pt.Y
		} else if pt.Y > tr.Y {
			tr.Y = pt.Y
		}
	}
	if math.Abs(ll.X-p1.MinX) > Zeroish || math.Abs(ll.Y-p1.MinY) > Zeroish || math.Abs(tr.X-p1.MaxX) > Zeroish || math.Abs(tr.Y-p1.MaxY) > Zeroish {
		return fmt.Errorf("bad bounding box [%d]: %#v - vs %v %v", n, p1, ll, tr)
	}
	return nil
}

func TestIsLeft(t *testing.T) {
	vs := []struct {
		a, b, c Point
		want    bool
	}{
		{Point{0, .1}, Point{0, 0}, Point{1, 0}, true},
		{Point{.1, 0}, Point{0, 0}, Point{0, 1}, false},
	}
	for i, v := range vs {
		got := v.a.isLeft(v.b, v.c)
		if got != v.want {
			t.Fatalf("test=%d error see %v is left=%v of %v->%v", i, v.a, v.want, v.b, v.c)
		}
	}
}

func TestMinMax(t *testing.T) {
	vs := []struct{ x, y, a, b float64 }{
		{x: 1, y: 2, a: 1, b: 2},
		{x: 2, y: 1, a: 1, b: 2},
		{x: -1, y: -2, a: -2, b: -1},
		{x: -1, y: 1, a: -1, b: 1},
	}
	for i, v := range vs {
		a, b := MinMax(v.x, v.y)
		if a != v.a || b != v.b {
			t.Errorf("test=%d MinMax(%f,%f) failed: got a=%f, b=%f, wanted a=%f, b=%f", i, v.x, v.y, a, b, v.a, v.b)
		}
	}
}

